import copy

def process(current_head, inss):
    # current location is [y, x]
    prev_head = current_head
    current_tail = [-1, -1]
    tails_visited = []
    for ins in inss:
        direction = ins.split(" ")[0]
        number_of_steps = int(ins.split(" ")[1])
        for i in range(number_of_steps):
            prev_head = current_head
            if direction == "R":
                current_head = [current_head[0], current_head[1] + 1]
            elif direction == "L":
                current_head = [current_head[0], current_head[1] - 1]
            elif direction == "U":
                current_head = [current_head[0] - 1, current_head[1]]
            elif direction == "D":
                current_head = [current_head[0] + 1, current_head[1]]

            y_diff = current_head[0] - current_tail[0]
            x_diff = current_head[1] - current_tail[1]

            if abs(y_diff) > 1 or abs(x_diff) > 1:
                current_tail = prev_head
                if current_tail not in tails_visited:
                    tails_visited.append(current_tail)

    print(f"current_head-{current_head}, current_tail-{current_tail}")
    print(f"tail visited - {tails_visited}")
    return tails_visited

def process_tails(current_head, number_of_tails, current_tail, inss):
    # current location is [y, x]
    tails = []
    for i in range(number_of_tails):
        tails.append(copy.copy(current_tail))

    tails_visited = []
    for ins in inss:
        direction = ins.split(" ")[0]
        number_of_steps = int(ins.split(" ")[1])
        for i in range(number_of_steps):
            if direction == "R":
                current_head = [current_head[0], current_head[1] + 1]
            elif direction == "L":
                current_head = [current_head[0], current_head[1] - 1]
            elif direction == "U":
                current_head = [current_head[0] - 1, current_head[1]]
            elif direction == "D":
                current_head = [current_head[0] + 1, current_head[1]]

            front = current_head

            for ti in range(len(tails)):
                back = tails[ti]
                y_diff = front[0] - back[0]
                x_diff = front[1] - back[1]
                move_tail = False

                if abs(y_diff) > 1:
                    if abs(x_diff) == 0:
                        back[0] = back[0] + (-1 if y_diff < 0 else 1)
                    else:
                        back[0] = back[0] + (-1 if y_diff < 0 else 1)
                        back[1] = back[1] + (-1 if x_diff < 0 else 1)
                    move_tail = True
                elif abs(x_diff) > 1:
                    if abs(y_diff) == 0:
                        back[1] = back[1] + (-1 if x_diff < 0 else 1)
                    else:
                        back[0] = back[0] + (-1 if y_diff < 0 else 1)
                        back[1] = back[1] + (-1 if x_diff < 0 else 1)
                    move_tail = True

                # reset front
                front = back

                if move_tail:
                    current_tail = copy.copy(tails[len(tails) - 1])

                    if current_tail != current_head and current_tail not in tails_visited:
                        tails_visited.append(current_tail)

    return tails_visited

