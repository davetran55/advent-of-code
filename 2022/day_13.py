def parse_line(s: str, index: int, l: list):

    while index < len(s):
        if s[index].isnumeric():
            # keep reading all numerics
            s_num = ''
            while index < len(s) and s[index].isnumeric():
                s_num = s_num+s[index]
                index += 1
            index -= 1
            l.append(int(s_num))
        else:
            if s[index] == '[':
                new_l = list()
                index = parse_line(s, index+1, new_l)
                l.append(new_l)
            elif s[index] == ']':
                return index
        index += 1

    return l

